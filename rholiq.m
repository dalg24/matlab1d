function [CRHOLI]=rholiq( HL, PRE )
% C=======================================================================
% C
% C     Calcul de la masse volumique liquide en fonction de P et HL
% C
% C=======================================================================
% *>HL      Enthalpie liquide 
% *>PRE     Pression en Pa
% C=======================================================================

R(1)= 0.50507E-30;
R(2)=-0.58821E-22;
R(3)=-0.83829E-24;
R(4)= 0.17239E-15;
R(5)= 0.85471E-18;
R(6)=-0.29179E-09;
R(7)= 0.48157E-06;
R(8)= 0.99916E+03;

RR(1)=R(1)*PRE+R(2);
RR(2)=R(3)*PRE+R(4);
RR(3)=R(5)*PRE+R(6);
RR(4)=R(7)*PRE+R(8);

% C=======================================================================

CRHOLI=((RR(1)*HL+RR(2))*HL+RR(3))*HL^2+RR(4);
